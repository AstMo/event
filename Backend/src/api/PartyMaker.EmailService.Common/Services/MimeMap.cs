        public string GetMimeType(string extension)
        {
            if (extension == null)
                throw new ArgumentNullException("extension");

            if (!extension.StartsWith("."))
                extension = "." + extension;

            string mime;
            return _mappings.TryGetValue(extension, out mime) ? mime : null;
        }

        public string GetExtension(string mimeType)
        {
            if (mimeType == null)
                throw new ArgumentNullException("mimeType");

            string extension;

            if (_mappings.TryGetValue(mimeType, out extension))
                return extension;

            return null;
        }
